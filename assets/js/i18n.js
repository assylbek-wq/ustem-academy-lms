const i18n = {
  get lang() {
    const saved = localStorage.getItem(I18N_KEY);
    return AVAILABLE_LANGS.includes(saved) ? saved : DEFAULT_LANG;
  },
  setLang(lang) {
    if (!AVAILABLE_LANGS.includes(lang)) return;
    localStorage.setItem(I18N_KEY, lang);
    document.documentElement.lang = lang;
    this.apply();
    document.title = this.t(document.body.dataset.titleKey || 'home.title') || document.title;
  },
  t(key, params) {
    const dict = translations[this.lang] || translations[DEFAULT_LANG];
    let s = dict[key] ?? translations[DEFAULT_LANG][key] ?? key;
    if (params) {
      Object.keys(params).forEach(k => {
        s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), params[k]);
      });
    }
    return s;
  },
  apply(root = document) {
    // text content
    root.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = this.t(key);
    });
    // attribute translations: data-i18n-placeholder, data-i18n-title, data-i18n-aria-label
    ['placeholder', 'title', 'aria-label', 'value', 'alt'].forEach(attr => {
      root.querySelectorAll(`[data-i18n-${attr}]`).forEach(el => {
        const key = el.getAttribute(`data-i18n-${attr}`);
        el.setAttribute(attr, this.t(key));
      });
    });
    // html content (allows tags)
    root.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      el.innerHTML = this.t(key);
    });
  },
  // Localized field helper: { ru: '..', kk: '..', en: '..' } → string
  loc(obj) {
    if (typeof obj === 'string') return obj;
    if (!obj) return '';
    return obj[this.lang] || obj.ru || obj.en || Object.values(obj)[0];
  },
};

// Set initial html lang attribute
if (typeof document !== 'undefined') {
  document.documentElement.lang = i18n.lang;
}

window.i18n = i18n;
window.t = (key, params) => i18n.t(key, params);
window.loc = obj => i18n.loc(obj);
